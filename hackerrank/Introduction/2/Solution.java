import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int i;
        for (i = 0; i < 3; ++i) {
            int a = scan.nextInt();
            System.out.println(a);
        }
        scan.close();
    }
}

